'''     Diferença
    Ler quatro valores inteiros e armazenar nas variáves A, B, C e D.
     Calcule e imprima a diferença do produto A e B pelo produto de C e D (A * B - C * D).

    Formato do resultado(saída) Ex: DIFERENCA = -26
'''

A = int(input())
B = int(input())
C = int(input())
D = int(input())
DIFERENCA = (A * B - C * D)
print('DIFERENCA = {}'.format(DIFERENCA))